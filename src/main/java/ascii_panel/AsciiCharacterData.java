package ascii_panel;

import java.awt.Color;

/**
 * Class that defines the components of the ascii char.
 * @author danie
 *
 */
public class AsciiCharacterData {
	
	/**
	 * char containing the specific ascii char.
	 */
	public char character;
	
	/**
	 * Color for the foreground of the char.
	 */
	public Color foregroundColor;
	
	/**
	 * Color for the background of the char.
	 */
	public Color backgroundColor;
	
}